def norm_kana(text: str) -> str:
    s = text.strip()
    out = []
    while s:
        if len(s) >= 2 and s[:2] in KANA_TABLE:
            out.append(KANA_TABLE[s[:2]])
            s = s[2:]
        elif s[0] in KANA_TABLE:
            out.append(KANA_TABLE[s[0]])
            s = s[1:]
        else:
            out.append(s[0])
            s = s[1:]
    return "".join(out)
